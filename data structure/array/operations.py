fruits = list()
fruits = ['Banana', 'Cherry', 'Avocado']
print(fruits)

# Add
fruits.append('Pineapple')
print(fruits)

# Pop
fruits.pop()
print(fruits)

# Sort
fruits.sort()
print(fruits)

# Insert
fruits.insert(2, 'Coco')
print(fruits)

# List Comprehension
print('Create List Comprehension')
sqare = list(map(lambda x: x**2, range(10)))
print(sqare)

